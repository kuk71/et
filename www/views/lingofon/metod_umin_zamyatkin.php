<h3>Продолжительность занятий.</h3>
<p>
    Лингафонный курс разбит на уроки продолжительностью от 20 секунд до полутора минут. На каждом занятии Вам нужно
    брать уроки, суммарная длительность которых не более полутора минут. Тогда Вы сможете освоить материал примерно за
    час.</p>
<p>
    Замечание: не следует пренебрегать пунктом 4. Проще сразу сформировать правильное произношение чем потом исправлять
    неверное. Это точно без преподавателя не получится.</p>
<p>
    Примерно через 3 месяца таких занятий Вы сможете свободно понимать английскую речь и уверенно объясняться на
    английском языке.</p>
