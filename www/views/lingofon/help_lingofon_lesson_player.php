<p>По умолчанию урок воспроизводится по одному предложению. И каждое предложение автоматически повторяется. Чтобы
    перейти к следующему предложению нажмите на кнопку "<b>Далее >>></b>" если хотите вернуться к предыдущему на кнопку
    "<b><<<</b>"</p>
<p>Если хотите, чтобы урок проигрывался не по одному предложению, а сразу весь, нажмите на кнопку "<b>Проигрывать&nbsp;всё</b>".
    Надпись на кнопке изменится на "<b>По одному</b>". Если Вы захотите снова слушать урок по одному предложению,
    нажмите на кнопку "<b>По одному</b>"</p>
<p>Если Вы захотите выключить повторение нажмите на кнопку "<b>Без&nbsp;повтора</b>". Надпись на кнопке изменится на
    "<b>Повторять</b>". Что бы снова включить повторение нажмите на кнопку "<b>Повторять</b>"</p>
<p>Если Вы хотите увидеть текст предложения, которое воспроизводится в данный момент нажмите на кнопку "<b>Текст +</b>".
    Надпись на кнопке изменится на "<b>Текст -</b>" и на экране появится текст предложения.</p>
