<p>Что бы убрать текст с экрана нажмите на кнопку "<b>Текст -</b>"</p>
<p>Если Вы хотите увидеть перевод предложения, которое воспроизводится в данный момент нажмите на кнопку "<b>Перевод
        +</b>". Надпись на кнопке изменится на "<b>Перевод -</b>" и на экране появится перевод предложения.</p>
