<p>Что бы начать воспроизведение нужно нажать на кнопку "<b>Play</b>". Надпись на кнопке изменится на "<b>Stop</b>" и
    начнется воспроизведение урока. Что бы остановить воспроизведение нажмите на кнопку "<b>Stop</b>"</p>
<p>По умолчанию скорость воспроизведения снижена до 60%. Если Вы захотите изменить скорость воспроизведения, нажмите на
